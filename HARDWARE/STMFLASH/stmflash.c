void UpDataViaSDCard (FILINFO *fno)
{
    FIL fil;
    FRESULT fr;
    char ch;
    uint8_t tc,d=0,line[24],*tcode,*p,crc;
    
    uint16_t endl,offset=0;
    uint32_t writeaddr,rb;
    int i=0,j=0;
    
    fr=f_open(&fil,fno->fname,FA_READ);
    if(fr!=FR_OK)
        return;
    memset(line,0,24);
    //tcode=(uint8_t*)malloc(512);
    //p=tcode;
    STMFLASH_Read(0x08000000,(uint16_t*)0x20000000,0x200);
    SCB->VTOR = 0x20000000; //更改中断向量表
    do
    {
        fr=f_read(&fil,&ch,1,&rb);
        if(fr!=FR_OK || rb==0)
        endl<<=8;
        endl|=ch;
        if(endl==0x0d0a)
        {
            if(line[0]==':')
            {
                crc=0;
                for(j=0;j<(line[1])+4;j++)
                {
                    crc+=line[j+1];
                }
                crc=~crc+1;
                if(crc!=line[line[1]+5])
                {
                    goto nextline;
                }  
                switch(line[4])
                {
                    case 0:
                        offset=line[2]<<8;
                        offset|=line[3];
                        writeaddr=writeaddr&0xffff0000;
                        writeaddr|=offset;
                        STMFLASH_Write(writeaddr,(uint16_t*)(line+5),line[1]/2);
                        break;
                    case 4:
                        writeaddr=line[5]<<24;
                        writeaddr|=line[6]<<16;
                        break;
                    case 1:
                        break;
                    default:
                        break;
                }
            }
            //do write flash
            nextline:
            endl=0;
            d=0;
            i=0;
            memset(line,0,24);
            continue;
        }
        if((ch>='0'&&ch<='9')||(ch>='A'&&ch<='F'))
        {
            if(d==0)
            {
                d=1;
                if(ch>='A')
                {
                    tc=(ch-'A'+10)<<4;
                }
                else
                {
                    tc=(ch-'0')<<4;
                }
            }
            else
            {
                d=0;
                if(ch>='A')
                {
                    tc|=(ch-'A'+10);
                }
                else
                {
                    tc|=(ch-'0');
                }
                line[i++]=tc;
            }
        }
        else
        {
            line[i++]=ch;
        }
    }
    while(ch!=EOF);
    f_close(&fil);
    
    __DSB();                                                          /* Ensure all outstanding memory accesses included
                                                                       buffered write are completed before reset */
    SCB->AIRCR  = (uint32_t)((0x5FAUL << SCB_AIRCR_VECTKEY_Pos)    |
                           (SCB->AIRCR & SCB_AIRCR_PRIGROUP_Msk) |
                            SCB_AIRCR_SYSRESETREQ_Msk    );         /* Keep priority group unchanged */
    __DSB();                                                          /* Ensure completion of memory access */

    for(;;)                                                           /* wait until reset */
    {
        __NOP();
    }
}













